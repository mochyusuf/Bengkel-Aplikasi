    void BuatTableRincianService()
    {
        try{
            Statement stmr;
            stmr=con.createStatement();
            stmr.executeUpdate("insert into rincian_service select trans_service.id_barang, trans_service.id_service,"
                    + "jasa_service.nama_service, jasa_service.biaya_service, "
                    + "user.id_user as id_mekanik , pendaftaran.id_pendaftaran,barang.nama_barang,"
                    + "barang.kategori_barang, barang.kualitas,barang.merk,barang.harga_jual,"
                    + "barang.warna, trans_service.stock_service, barang.satuan,"
                    + "barang.id_supplier, barang.tanggal_terima, "
                    + "(barang.harga_jual*trans_service.stock_service) as jumlah ,"
                    + "barang.stock from trans_service "
                    + "inner join barang on barang.id_barang=trans_service.id_barang "
                    + "INNER join jasa_service on jasa_service.id_service=trans_service.id_service "
                    + "inner JOIN pendaftaran on pendaftaran.id_pendaftaran=trans_service.id_pendaftaran "
                    + "INNER JOIN user on user.id_user=trans_service.id_mekanik");
        
        }catch(Exception e){
            e.printStackTrace();
                    
        }
    }
    
