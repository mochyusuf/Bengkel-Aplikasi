    void KodeFakturPendaftaran()
    {
        try{
            String FakturPBTerakhir = null;
            Statement stm; 
            stm=con.createStatement();
            rsmekanik=stm.executeQuery("SELECT id_pendaftaran FROM pendaftaran WHERE "
                                        + "id_pendaftaran IN "
                                        + "( SELECT MAX(id_pendaftaran) FROM "
                                        + "pendaftaran )");
            while(rsmekanik.next()){
             FakturPBTerakhir = rsmekanik.getString("id_pendaftaran");
            }
            String TambahFaktur1 = FakturPBTerakhir.substring(4);
            Integer TambahFaktur2 = Integer.parseInt(TambahFaktur1);
            Integer TambahFaktur3 = TambahFaktur2 + 1;
            String TambahFaktur4 = Integer.toString(TambahFaktur3);
            Integer PanjangString = TambahFaktur4.length();
            Integer TambahanString = 10 - PanjangString;
            String tambahan = "0";
            for (int counter=1; counter<=TambahanString; counter++)
            {
                    TambahFaktur4 = tambahan + TambahFaktur4;
            }
            String FakturPBTerbaru = "IDP" + TambahFaktur4;
            JT_K_PSC_ID_Pendaftaran.setText(FakturPBTerbaru);
        }catch(SQLException e){
        }
    }
    
